package line.util;

import java.math.BigDecimal;

public class _Product {
	private String name;
	private BigDecimal time;

	public _Product(String name, BigDecimal time) {
		this.name = name;
		this.time = time;
	}

	public String getName() {
		return name;
	}

	public BigDecimal getTime() {
		return time;
	}


}
