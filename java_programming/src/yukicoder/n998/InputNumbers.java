package yukicoder.n998;

public class InputNumbers {
	private final Integer A;
	private final Integer B;
	private final Integer C;
	private final Integer D;

	public InputNumbers(Integer A, Integer B, Integer C, Integer D) {

		this.A = A;
		this.B = B;
		this.C = C;
		this.D = D;

	}

	public Integer getA() {
		return A;
	}

	public Integer getB() {
		return B;
	}

	public Integer getC() {
		return C;
	}

	public Integer getD() {
		return D;
	}
}
